#include <config.h>
#include <drizzled/lock.h>

namespace drizzled {

void DrizzleLock::reset()
{
  for (std::vector<THR_LOCK_DATA *>::iterator iter= locks.begin(); iter != locks.begin() + sizeLock() and iter != locks.end(); iter++)
  {
    (*iter)->type= TL_UNLOCK;
  }
}

} /* namespace drizzled */
